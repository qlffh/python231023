# List example
my_list = [1, 2, 3, 4, 5]
print("List Example:")
print(f"Original list: {my_list}")
print(f"Element at index 2: {my_list[2]}")
print()

# Tuple example
my_tuple = (1, 2, 3, 4, 5)
print("Tuple Example:")
print(f"Original tuple: {my_tuple}")
print(f"Count of 3: {my_tuple.count(3)}")
print()

# Dictionary example
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}
print("Dictionary Example:")
print(f"Original dictionary: {my_dict}")
print(f"Value for key 'c': {my_dict['c']}")
print()

# Set example
my_set = {1, 2, 3, 4, 5}
print("Set Example:")
print(f"Original set: {my_set}")
my_set.add(6)
print(f"Updated set: {my_set}")
